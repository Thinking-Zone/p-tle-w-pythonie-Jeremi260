# Pętla "for" jest zazwyczaj używana, gdy znamy liczbę iteracji lub gdy chcemy iterować po elementach kolekcji (np. listy, krotki, ciągi znaków, zakresy itp.)
# Przykład pętli for:
# for i in range(5):  # iteracja po liczbach 0 do 4
#     print(i)

# Pętla "while" jest używana, gdy chcemy, aby pętla wykonywała się dopóki nie spełni się jakiś warunek. 
# Używamy jej, gdy nie znamy dokładnej liczby iteracji na początku i zależymy od jakiegoś warunku.
# Przykład pętli while:
# i = 0
# while i < 5:  # pętla będzie się wykonywać, dopóki i jest mniejsze od 5
#     print(i)
#     i += 1
